/** Devuelve una copia ordenada alfabÃ©ticamente por nombre (ES). */
export function sortCountriesByName(list = countriesAmericas) {
  return [...list].sort((a, b) => a.name.localeCompare(b.name, 'es'));
}

/** Busca un paÃ­s por cÃ³digo ISO-2 (e.g., "MX") */
export function findCountryByCode(code, list = countriesAmericas) {
  if (!code) return null;
  const up = code.toUpperCase();
  return list.find((c) => c.code === up) || null;
}

/** Etiqueta estÃ¡ndar para selects: "ğŸ‡²ğŸ‡½ MÃ©xico (+52)" */
export function countryLabel(country) {
  if (!country) return '';
  return `${country.flag} ${country.name} (${country.dial_code})`;
}

/** Lista preparada para <v-select>: { title, value } */
export function toVuetifyItems(list = countriesAmericas) {
  return sortCountriesByName(list).map((c) => ({
    title: countryLabel(c),
    value: c.code
  }));
}

